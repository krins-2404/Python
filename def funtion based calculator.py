x= int(input("The Enter first number:"))
y= int(input("The Enter Second Number:"))
def add(x,y):
    return x+y

print ("The Addition of x+y:",add(x,y))


def subtract(x,y):
    return x-y

print ("The Subtraction of x-y:",subtract(x,y))


def multiple(x,y):
    return x*y

print ("The Multiplication of x*y:",multiple(x,y))


def divide(x,y):
    return x/y

print ("The Division of x/y:",divide(x,y))


def average(x,y):
    return (x+y)/2

print ("The Average of x and y:",average(x,y))
